     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        if(Aluno::count() != 0){
            $alunos = Aluno::orderBy('nome_aluno')->get();
        }else{
            $alunos = null;
        }
        return view('aluno.index')->with('alunos',$alunos);
    }

